(() => {
  // <stdin>
  var canvas = document.querySelector("#canvas-1");
  var iter = 0;
  function drawLogo() {
    let ctx = canvas.getContext("2d");
    let a = Math.abs(Math.sin(iter)) * 200;
    let b = Math.abs(Math.cos(iter)) * 200;
    let c = Math.abs(Math.sin(iter)) * 200;
    let d = Math.abs(Math.cos(iter)) * 200;
    let gradient = ctx.createLinearGradient(a, b, 200, 200);
    gradient.addColorStop(0, "red");
    gradient.addColorStop(0.5, "green");
    gradient.addColorStop(1, "blue");
    ctx.strokeStyle = gradient;
    const lineWidth = 8;
    ctx.lineWidth = lineWidth;
    let xStart = 45;
    let yStart = 120;
    let x = xStart;
    let y = yStart;
    ctx.beginPath();
    ctx.moveTo(x, y);
    x -= 25;
    ctx.lineTo(x, y);
    y += 25 + lineWidth / 2;
    ctx.lineTo(x, y);
    ctx.stroke();
    y += 25 - lineWidth / 2;
    x += 25 - lineWidth / 2;
    ctx.beginPath();
    ctx.moveTo(x, y);
    x += 25 + lineWidth / 2;
    ctx.lineTo(x, y);
    y -= 100 + lineWidth / 2;
    ctx.lineTo(x, y);
    x -= 50;
    ctx.lineTo(x, y);
    y -= 50;
    ctx.lineTo(x, y);
    x += 125 + lineWidth / 2;
    ctx.lineTo(x, y);
    ctx.stroke();
    x += 25;
    y += 25;
    ctx.beginPath();
    ctx.moveTo(x, y);
    y += 25;
    ctx.lineTo(x, y);
    x -= 50;
    ctx.lineTo(x, y);
    y += 100 + lineWidth / 2;
    ctx.lineTo(x, y);
    x += 50;
    ctx.lineTo(x, y);
    y -= 50;
    ctx.lineTo(x, y);
    x -= 25;
    ctx.lineTo(x, y);
    ctx.stroke();
    ctx.beginPath();
    ctx.arc(xStart - 25, yStart + 50, 25, 1.5 * Math.PI, 2 * Math.PI, false);
    ctx.stroke();
    ctx.beginPath();
    ctx.arc(xStart + 125, yStart - 100, 25, 0.5 * Math.PI, 1 * Math.PI, false);
    ctx.stroke();
    ctx.beginPath();
    ctx.arc(xStart - 25, yStart + 50, 12.5, 0, 2 * Math.PI, false);
    ctx.stroke();
    ctx.beginPath();
    ctx.arc(xStart + 125, yStart - 100, 12.5, 0, 2 * Math.PI, false);
    ctx.stroke();
  }
  window.onload = function() {
    setInterval(() => {
      drawLogo();
      iter += 0.1;
    }, 100);
  };
})();
